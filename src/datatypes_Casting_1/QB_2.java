package datatypes_Casting_1;

public class QB_2 {
public static void main(String[] args) {
	
	// primitive data type 

	int a = 10;
	short b = 12;
	long c =1234567893l;
	float d =12.5f;
	double e = 123.45;
	char f ='A';
	byte g =3;
	boolean h =true;
	
	// wapper class 
	
	Integer  intObj =a ;
	Short  shortObj =b ;
	Long longObj =c;
	Float floatObj =d;
	Double doubleObj =e;
	Character charObj =f;
	Byte byteObj =g;
	Boolean booleanObj =h;
	
	// print 
	System.out.println(intObj);
	System.out.println(shortObj);
	System.out.println(longObj);
	System.out.println(floatObj);
	System.out.println(doubleObj);
	System.out.println(charObj);
	System.out.println(byteObj);
	System.out.println(booleanObj);
}
}
